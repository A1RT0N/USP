def aux_height(root:Node):
    if (not root):
        return -1
    right_h = aux_height(root.right)
    left_h = aux_height(root.left)
    
    return max(right_h, left_h) + 1


def height(root:Node):
    if (root.left and root.right):
        return 0
    else:
        return aux_height(root)
    
    


